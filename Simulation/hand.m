function [A]=hand()
x=[ 0.0;  3.0;  5.6;  5.6;  5.6; ...
    2.0;  2.7;  2.7;  2.7;  2.7; ...
    1.2;  1.2;  1.2;  1.2;  1.2; ...
   -0.4; -0.4; -0.4; -0.4; -0.4; ...
   -1.1; -1.5; -1.5; -1.5; -1.5  ];
 
y=[ 0.0; 1.6;  6.3;  9.4; 11.8; ...
    2.3; 7.7; 13.2; 15.7; 18.3; ...
    2.4; 7.7; 14.2; 17.2; 20.0; ...
    2.3; 7.7; 13.2; 15.9; 19.0; ...
    2.4; 7.5; 11.2; 13.4; 16.0  ];

z=zeros(25, 1);
A=[x y z];

angle=zeros(25,3);
angle(12,:)=[pi/6,0,0];
angle(13,:)=[pi/6,0,0];
angle(14,:)=[pi/6,0,0];


D=[0,0,1,2,3,4; ...
   0,5,6,7,8,9; ...
   0,10,11,12,13,14; ...
   0,15,16,17,18,19; ...
   0,20,21,22,23,24];
 
E=zeros(25,25);
for i=0:4
    for j=0:4
        k=5*i+j+1;
        E(k, 1+D(i+1, j+1))=-1;
        E(k, 1+D(i+1, j+2))=1;
    end
end
B=E*A;

disp(B(2,:));

C=zeros(25, 3);
for i=0:4
    R=eye(3);
    for j=0:4
        k=5*i+j+1;
        R=R*yawPitchRoll(angle(k,1), angle(k,2), angle(k,3));    
        C(k,:)=B(k,:)*R;
        if j>0
            C(k,:)=C(k,:)+C(k-1,:);
        end
    end
end

scatter3(C(:,1), C(:,3), C(:,2));  
end